package practiseByMKT;

public class Static_methods {
	
	static void country(String country)
	{
		System.out.println("Beautiful Countries in the world");
	}
	
	static void india()
	{
		System.out.println("India");
	}
	
	static void france()
	{
		System.out.println("France");
	}

	static void england()
	{
		System.out.println("England");
	}
	
	static void poland()
	{
		System.out.println("Poland");
	}
	
	static void usa()
	{
		System.out.println("USA");
	}
	
	public static void main(String[] args) {
		
		country(null);
		india();
		france();
		england();
		poland();
		usa();

	}

}
